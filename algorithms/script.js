// Найти индекс заданного числа в массиве: {3, 6, 4, 7, 2, 1, 9}
// Алгоритм на вход должен получать любой массив и одну цифру, индекс
// которой требуется найти.

const array = [3, 6, 4, 7, 2, 1, 9];

function linearSearch(arr, item) {
    for (let i = arr.length - 1; i >= 0; i--) {
        if (arr[i] === item) {
            return `Item Index : ${i}`;
        }
    }
    return null
}

console.log(linearSearch(array, 4))

// Задание считается выполненным если: использован ”Линейный подход”
//  начиная от последнего элемента
// и алгоритм соответствует характеристикам алгоритма и выдает
