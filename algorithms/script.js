// Level 2
// Реализовать алгоритм, который будет
// находить сумму квадратов всех элементов массива!

const squaredArray = [3, 6, 4, 7, 2, 1, 9];

function squared(array){
    const total = array.reduce((total, item) => total + item ** 2, 0);
    return `Squared Sum : ${total}`
}

console.log(squared(squaredArray))

